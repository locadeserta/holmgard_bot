# Обработчик перевода
async def translate(update: Update, context: ContextTypes.DEFAULT_TYPE):
    import re  # Для обработки текста

    user_input = update.message.text.strip().lower()  # Приводим текст к нижнему регистру
    chat_type = update.message.chat.type  # Тип чата (private, group, supergroup и т.д.)
    excluded_words = ["я", "но", "и", "ты", "если", "чтобы", "год", "так", "очень", "слово", "есть", "столько", "кроме", "сам"]  # Список слов, на которые бот не должен реагировать в группах
    result = None
    response = None

    # Разбиваем текст на слова и проверяем их одно за другим
    words = re.findall(r'\b\w+\b', user_input)

    for word in words:
        if chat_type in ["group", "supergroup"] and word in excluded_words:
            continue  # Пропускаем слова из excluded_words в группах

        if word.capitalize() in translations:  # Используем capitalized для точного поиска
            result = translations[word.capitalize()]
            контекст = result.get("контекст")
            перевод = result.get("перевод")

            if контекст and перевод:
                response = f"{контекст} — {', '.join(перевод)}" if isinstance(перевод, list) else f"{контекст} — {перевод}"
            elif isinstance(перевод, list):
                response = f"{word} — {', '.join(перевод)}"
            else:
                response = f"{word} — {перевод}"
            break  # Если нашли совпадение, прекращаем обработку

    if response:
        await update.message.reply_text(response)
    elif chat_type == "private":
        # Отправляем сообщение "Такого слова в боте нет" только в личных чатах
        await update.message.reply_text("Такого слова в боте нет.")

